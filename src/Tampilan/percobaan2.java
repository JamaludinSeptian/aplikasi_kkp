    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
          String sql = "INSERT INTO deliveryorder values(?,?,?,?,?)";
        try {
            PreparedStatement stat = con.prepareStatement(sql);
            stat.setString(1, tIdSuratJalan.getText());
            stat.setString(2, tPengirim.getText());
            stat.setString(3, tPenerima.getText());
            stat.setString(4, tAlamatPengirim.getText());
            stat.setString(5, tAlamatPenerima.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil di Simpan");
            kosong();
            datatable();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal di Simpan"+e);
        }
    }//GEN-LAST:event_bSimpanActionPerformed

