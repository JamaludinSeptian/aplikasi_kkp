    protected void datatable() {
        Object[] Baris={"ID Detail","ID Surat Jalan","ID Barang","Jumlah"};
        tabmode=new DefaultTableModel(null,Baris);
        tableOrderDetail.setModel(tabmode);
        String sql="SELECT * FROM deliveryorderdetail";
        
        try{
            java.sql.Statement stat=con.createStatement();
            ResultSet hasil=stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("id_detail");
                String b = hasil.getString("id_surat_jalan");
                String c = hasil.getString("id_barang");
                String d = hasil.getString("jumlah");
              
                String[] data={a,b,c,d};
                tabmode.addRow(data);
            }
        }catch(Exception e){
            
        }
    }
    
