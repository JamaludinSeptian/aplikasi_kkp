    protected void datatable() {
        Object[] Baris={"ID Surat Jalan","Pengirim","Penerima","Alamat Pengirim","Alamat Penerima"};
        tabmode=new DefaultTableModel(null,Baris);
        tableSuratJalan.setModel(tabmode);
        String sql="SELECT * FROM deliveryorder";
        
        try{
            java.sql.Statement stat=con.createStatement();
            ResultSet hasil=stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("id_surat_jalan");
                String b = hasil.getString("pengirim");
                String c = hasil.getString("penerima");
                String d = hasil.getString("alamat_pengirim");
                String e = hasil.getString("alamat_penerima");
              
                String[] data={a,b,c,d,e};
                tabmode.addRow(data);
            }
        }catch(Exception e){
            
        }
    }
    
    
