    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        String idsuratjalan = jIdSuratJalan.getSelectedItem().toString();
        String idbarang = jIdBarang.getSelectedItem().toString();
        String sql = "INSERT INTO deliveryorderdetail values(?,?,?,?)";
        try {
            PreparedStatement stat = con.prepareStatement(sql);
            stat.setString(1, tIdDetail.getText());
            stat.setString(2, idsuratjalan);
            stat.setString(3, idbarang);
            stat.setString(4, tJumlah.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil di Simpan");
            kosong();
            datatable();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal di Simpan"+e);
        }
    }//GEN-LAST:event_bSimpanActionPerformed

